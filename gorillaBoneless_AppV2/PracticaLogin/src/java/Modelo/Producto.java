package Modelo;

/**
 *
 * @author lv1822
 */
public class Producto {
    private int id;
    private String nombre;
    private String img_producto;
    private double precio;
    private int categoria;

    public Producto() {
    }

    public Producto(int id, String nombre, String img, double precio, int categoria) {
        this.id = id;
        this.nombre = nombre;
        this.img_producto = img;
        this.precio = precio;
        this.categoria = categoria;
    }
    
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getImg_producto() {
        return img_producto;
    }

    public void setImg_producto(String img_producto) {
        this.img_producto = img_producto;
    }

    public double getPrecio() {
        return precio;
    }

    public void setPrecio(double precio) {
        this.precio = precio;
    }

    public int getCategoria() {
        return categoria;
    }

    public void setCategoria(int categoria) {
        this.categoria = categoria;
    }

}
